            <section
              class="av_textblock_section av-6rzmeir-b92a35d5d7d0d407bd1b65b53e143ec2"
              itemscope="itemscope"
              itemtype="https://schema.org/CreativeWork"
            >
              <div class="avia_textblock" itemprop="text">
                <p>
                  <strong>Abandonment Clause:</strong> A clause often contained
                  in a property insurance policy stating that the insured cannot
                  abandon damaged property and then file a claim with an
                  insurer.
                </p>
                <p>
                  <strong>Absolute Liability:</strong> The liability for damages
                  even though fault / negligence cannot be proven.
                </p>
                <p>
                  <strong>Accident:</strong> Any sudden event which is
                  unintended.
                </p>
                <p>
                  <strong>Accident Insurance:</strong> Insurance coverage
                  against loss by accidental bodily injury.
                </p>
                <p>
                  <strong>Accidental Bodily Injury:</strong> Injury to a person
                  from the result of an accident.
                </p>
                <p>
                  <strong>Accidental Death Benefit:</strong> An additional paid
                  death benefit in addition to the face amount value of a life
                  insurance policy. Accounts Receivable Coverage Form: An inland
                  marine coverage form that insures against loss the insured
                  suffers when not able to collect account receivables from
                  customers.
                </p>
                <p>
                  <strong>Accumulation Period:</strong> A specific time period
                  that the insured must establish before benefits begin or are
                  paid out.
                </p>
                <p>
                  <strong>Activities of Daily Living:</strong> Activities that
                  are considered an everyday part of normal life. Some of these
                  are: dressing, bathing, toileting, transferring (example:
                  moving from and into a chair), and eating. These activities
                  are used to measure the degree of impairment and can affect
                  the eligibility for certain types of insurance benefits.
                </p>
                <p>
                  <strong>Actual Cash Value (ACV):</strong> The cost to replace
                  an item or property at the time of loss, less any allowance
                  for depreciation.
                </p>
                <p>
                  <strong>Actuarial Cost Method:</strong> A method used for
                  determining contributions to be made under a retirement plan.
                  Usually applied to the level of benefits when the
                  contributions are fixed.
                </p>
                <p>
                  <strong>Actuary:</strong> A professional in the insurance
                  business, usually working for the insurance company, that can
                  estimate how a certain sum of money can be contributed to a
                  pension plan, insurance, or other related area to fund that
                  plan for years to come.
                </p>
                <p>
                  <strong>Additional Insured:</strong> An individual or entity
                  that is not included as an insured under the insurance policy
                  of another, but may be added to provide a certain degree of
                  insurance protection.
                </p>
                <p>
                  <strong>Adhesion (Contract of):</strong> Parties are of
                  unequal bargaining power, and one party (the insured) cannot
                  negotiate any terms, having to accept the offer of the other
                  party.
                </p>
                <p>
                  <strong>Adjustable Life Insurance:</strong> A type of life
                  insurance that allows the owner of a policy to change the plan
                  of insurance, raise or lower the face amount, increase or
                  decrease the premium, and lengthen or shorten the protection
                  period.
                </p>
                <p>
                  <strong>Adjusted Gross Estate:</strong> Approximate net worth
                  of a deceased, known as the beginning point for the
                  computation of estate taxes.
                </p>
                <p>
                  <strong>Adjuster:</strong> A person who investigates and
                  settles losses for an insurance company, or may be hired
                  independently to resolve any issues (leverage) between the
                  insurance company adjuster and the insured.
                </p>
                <p>
                  <strong>Adjusting:</strong> The investigation process of
                  settling claims by an insurance company.
                </p>
                <p>
                  <strong>Administrative Services Only (AS0) Plan:</strong> An
                  arrangement under which an insurance company or an independent
                  agent will, for a fee, handle the administration of claims,
                  benefits and other administrative functions for a self-insured
                  group. This is very popular with larger corporations.
                </p>
                <p>
                  <strong>Advance Funding:</strong> Pension funding method in
                  which an employer sets aside funds prior to the employee's
                  retirement.
                </p>
                <p>
                  <strong>Age Limits:</strong> Stipulated minimum and maximum
                  ages below and above which the company will not accept
                  applications or may not renew a policy. Read your policy.
                </p>
                <p>
                  <strong>Agent:</strong> An insurance company representative
                  licensed by the state who solicits, markets, negotiates,
                  binds, and administers contracts of insurance while providing
                  a valuable service to a policyholder for the insurer.
                </p>
                <p>
                  <strong>Aggregate Deductible:</strong> A deductible in some
                  property and health insurance contracts which all covered
                  losses during a year are figured together and an insurer pays
                  only when the aggregate deductible amount is exceeded.
                </p>
                <p>
                  <strong>Aggregate Indemnity:</strong> A maximum dollar amount
                  that can be collected for any disability or period of
                  disability under an insurance policy.
                </p>
                <p>
                  <strong>Alien Insurer:</strong> An insurance company domiciled
                  in another country.
                </p>
                <p>
                  <strong>Allocated Benefits:</strong> Benefits for which the
                  maximum amount payable for specific services is itemized in
                  your insurance contract.
                </p>
                <p>
                  <strong>All-Risk Policies:</strong> Coverage through an
                  insurance contract that promises to cover all losses except
                  those losses specifically excluded in your policy.
                </p>
                <p>
                  <strong>Alternate Delivery Systems:</strong> This system of
                  care is designed to provide needed services in a
                  cost-effective manner. This provides an insured with health
                  services other than an in-patient, acute-care hospital, or
                  other type of facility. Some examples include: skilled and
                  intermediary nursing facilities, hospice programs, and home
                  health care.
                </p>
                <p>
                  <strong>Ambulatory Care:</strong> These are medical services
                  that are provided as an outpatient (non-hospitalized).
                  Services could include diagnosis, treatment, and
                  rehabilitation.
                </p>
                <p>
                  <strong>Amendment:</strong> A formal document revising the
                  provisions of an insurance policy. Usually, signed jointly by
                  an insurance company officer and the policy owner or his
                  authorized representative.
                </p>
                <p>
                  <strong>Annual Statement:</strong> An annual report of an
                  insurance company to a state insurance department, showing
                  financial data relating to the operation of the insurance
                  company.
                </p>
                <p>
                  <strong>Annuitant:</strong> The person that will receive
                  annuity benefits for a period of time.
                </p>
                <p>
                  <strong>Annuity:</strong> Considered to be the opposite of
                  life insurance where a death benefit is paid, an annuity
                  provides a benefit while the insured is alive. This is a
                  contract that provides an income for a specified period of
                  time.
                </p>
                <p>
                  <strong>Annuity Certain:</strong> A contract that provides an
                  income for a specified number of years, regardless whether
                  living or deceased.
                </p>
                <p>
                  <strong>Annuity Consideration:</strong> A payment, or one of
                  the regular periodic payments, an annuitant makes for their
                  annuity.
                </p>
                <p>
                  <strong>Application:</strong> A signed statement of facts made
                  by a person applying for insurance. The application is used by
                  the insurance company to decide whether or not to issue a
                  policy. The application becomes part of the insurance contract
                  when the policy is issued.
                </p>
                <p>
                  <strong>Arson:</strong> The willful and malicious act of
                  burning, or attempt to burn, any structure or property,
                  usually with criminal or fraudulent intent.
                </p>
                <p>
                  <strong>Assets:</strong> Any funds, goods, property, rights of
                  actions, securities, or resources of any kind owned by an
                  insurance company.
                </p>
                <p>
                  <strong>Assignment:</strong> A legal transfer of one person's
                  interest in an insurance policy to another person.
                </p>
                <p>
                  <strong>Association Captive:</strong> A type of captive
                  insurer owned by the members of a sponsoring organization or
                  group, such as a trade association.
                </p>
                <p>
                  <strong>Association Group:</strong> A group formed from
                  members of a trade or a professional association for group
                  insurance under one master health insurance contract.
                </p>
                <p>
                  <strong>Association Group Plan:</strong> A health insurance
                  plan designed for the members of a professional association or
                  trade association. A members may be protected under a group
                  health insurance policy or by individual franchise policy
                  through this plan.
                </p>
                <p>
                  <strong>Assumptions:</strong> The many conditions and rules
                  underlying the calculation of a pension benefit.
                </p>
                <p>
                  <strong>Attractive Nuisance:</strong> Condition that can
                  attract and injure children. The occupants of land on which
                  such a condition exists are liable for injuries to children.
                  In Florida, pool owners are required to fence the area around
                  the pool.
                </p>
                <p>
                  <strong>Automatic Premium Loan:</strong> The cash borrowed
                  from a life insurance policy's cash value (to pay an overdue
                  premium).
                </p>
                <p>
                  <strong>Automobile Liability Insurance:</strong> Protection
                  for an insured against financial loss because of legal
                  liability act that has car related injuries to others or
                  damage to their property.
                </p>
                <p>
                  <strong>Automobile Physical Damage Insurance:</strong>
                  Coverage to pay for damage to, or loss, of an insured
                  automobile resulting from covered perils.
                </p>
                <p>
                  <strong>Automobile Shared Market:</strong> A program in which
                  all automobile insurers in each state make coverage available
                  to car owners who are unable to obtain auto insurance in the
                  voluntary market.
                </p>
                <p>
                  <strong>Aviation Insurance:</strong> Aircraft insurance
                  including coverage of aircraft or their contents. The owner's
                  liability, and accident insurance on the passengers can be
                  covered.
                </p>
