              <div class="avia_textblock" itemprop="text">
                <p>
                  <strong>Cafeteria Benefit Plan:</strong> An arrangement which
                  employees may select their own employee benefit structure.
                </p>
                <p>
                  <strong>Cancelable:</strong> A contract of insurance that may
                  be terminated during the policy term by the insurer or insured
                  at any time.
                </p>
                <p>
                  <strong>Cancellation:</strong> Termination of a policy in
                  force by a voluntary act of the insured or insurer.
                </p>
                <p>
                  <strong>Cancellation Clause:</strong> A provision in an
                  insurance contract that permits an insurance company or
                  insured to cancel a policy at any time before its expiration
                  date.
                </p>
                <p>
                  <strong>Capacity:</strong> The largest amount of insurance or
                  reinsurance available from an insurance company.
                </p>
                <p>
                  <strong>Capital Retention Approach:</strong> A method used to
                  estimate the amount of life insurance to own. Under this
                  method, the insurance proceeds are retained and are not
                  liquidated.
                </p>
                <p>
                  <strong>Capital Sum:</strong> The maximum lump sum payment
                  payable in the event of an accidental death or dismemberment.
                </p>
                <p>
                  <strong>Capitation:</strong> A method of payment for health
                  services in which a physician or hospital is paid a fixed, per
                  capita amount for each person served regardless of the actual
                  number of services provided to each person.
                </p>
                <p>
                  <strong>Captive Agent:</strong> A licensed insurance agent who
                  sells insurance for only one company.
                </p>
                <p>
                  <strong>Captive Insurance Company:</strong> A company formed
                  to insure the risks of a parent company. This is usually done
                  when business insurance for a certain commercial risk cannot
                  be obtained through markets.
                </p>
                <p>
                  <strong>Cargo Insurance:</strong> An insurance policy
                  protecting cargo being transported by carrier.
                </p>
                <p>
                  <strong>Cash Surrender Value:</strong> The amount of money
                  received when the policyowner surrenders a life insurance
                  policy with cash value.
                </p>
                <p>
                  <strong>Casualty Insurance:</strong> The type of insurance
                  concerned with the legal liability for losses caused by injury
                  to others or damage to property of others.
                </p>
                <p>
                  <strong>Catastrophe:</strong> An event which loss is of
                  extraordinary magnitude, such as a hurricane or tornado.
                </p>
                <p>
                  <strong>Causes of Loss Form:</strong> Commercial property
                  forms stating the perils insured against, additional
                  coverages, and exclusions that may apply to your policy. It is
                  important to read your policy.
                </p>
                <p>
                  <strong>Cede:</strong> The transfer of all or part of a risk
                  written by an insurer to a reinsurer.
                </p>
                <p>
                  <strong>Certificate of Insurance:</strong> A document issued
                  to a member of a group insurance plan, outlining the insurance
                  benefits and principal provisions of the policy.
                </p>
                <p>
                  <strong>Claim:</strong> A request by the insured for
                  indemnification by the insurance company for a loss that is a
                  covered peril.
                </p>
                <p>
                  <strong>Claims Made Policy:</strong> A liability insurance
                  policy under which a written claim is made during the policy
                  period or any extended reporting period.
                </p>
                <p>
                  <strong>Class Rating:</strong> A rate applied to those risks
                  that are similar.
                </p>
                <p>
                  <strong>Clause:</strong> In an insurance policy, sentences and
                  paragraphs describing coverage's, exclusions, duties of an
                  insured, and termination of coverage, and other such parts of
                  the insurance policy.
                </p>
                <p>
                  <strong>Coinsurance:</strong> Two meanings here: (1) In
                  property insurance, a clause that states the insured will
                  share in losses to the extent that he is underinsured at the
                  time of loss, (2) In medical insurance, the insured person and
                  the insurer share the covered procedures under a policy in a
                  specified ratio (80 percent by the insurer and 20 percent by
                  the insured).
                </p>
                <p>
                  <strong>Collision Insurance:</strong> A form of automobile
                  insurance, provides protection against loss resulting from any
                  damage to the policyowner's car caused by collision with
                  another vehicle or object, (or by upset of the insured car),
                  whether it was the insured's fault or not.
                </p>
                <p>
                  <strong>Combined Ratio:</strong> A measure of the dollars
                  spent for claims and expenses and premium dollars taken in.
                </p>
                <p>
                  <strong>Commercial General Liability Policy (CGL):</strong>
                  Provides separate limits of general liability, fire legal
                  liability, medical payments, products and completed
                  operations, and advertising and personal liability.
                </p>
                <p>
                  <strong>Commercial Lines:</strong> Insurance for businesses,
                  professionals, and commercial establishments.
                </p>
                <p>
                  <strong>Commercial Package Policy (CPP):</strong> A commercial
                  insurance policy that is designed to meet specific insurance
                  needs of businesses.
                </p>
                <p>
                  <strong>Completed Operations Insurance:</strong> A type of
                  insurance that covers a contractor's liability for accidents
                  arising out of jobs or operations that was completed by the
                  contractor.
                </p>
                <p>
                  <strong>Comprehensive Major Medical Insurance:</strong> A
                  health insurance policy that has a low deductible, high
                  maximum benefits, a coinsurance feature, and may feature a
                  copayment.
                </p>
                <p>
                  <strong>Comprehensive Personal Liability Insurance:</strong>
                  Provides individuals and family members with protection from
                  legal liability for most accidents caused by them in their
                  personal lives. Note that any legal liability claims submitted
                  while in the course of business activities are not covered.
                </p>
                <p>
                  <strong>Compulsory Insurance:</strong> Any form of insurance
                  which is required by law.
                </p>
                <p>
                  <strong>Concealment:</strong> Failure of an applicant for
                  insurance to reveal a material fact to the insurance company.
                </p>
                <p>
                  <strong>Conditional Binding Receipt:</strong> A receipt given
                  for premium payments accompanying an application for
