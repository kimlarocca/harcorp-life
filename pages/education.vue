              <div class="avia_textblock" itemprop="text">
                <p>
                  <strong>Bailee:</strong> Person or concern having the
                  possession of personal property entrusted to him by the owner
                  of said property.
                </p>
                <p>
                  <strong>Bailees Customers Insurance:</strong> A policy that
                  covers the loss or damage to personal property of customers.
                </p>
                <p>
                  <strong>Beneficiary:</strong> The designation by the owner of
                  a life insurance policy that indicates who will receive the
                  proceeds upon the insured's death or when a policy endowment
                  matures.
                </p>
                <p>
                  <strong>Benefit Period:</strong> The period of time which an
                  insurance company pays benefits to the named insured or
                  dependents.
                </p>
                <p>
                  <strong>Benefits:</strong> Monetary sum payable by the
                  insurance company to a claimant, assignee, or beneficiary.
                </p>
                <p>
                  <strong>Binder:</strong> A written or oral contract issued
                  temporarily to place insurance in force until a permanent
                  policy is issued. When an oral binder is issued, you should
                  request a written one to follow.
                </p>
                <p>
                  <strong>Binding Receipt:</strong> The (evidence or) receipt
                  given for a premium payment accompanying an application for
                  insurance.
                </p>
                <p>
                  <strong>Blanket Contract:</strong> A policy that covers an
                  insured's property at several different locations.
                </p>
                <p>
                  <strong>Boiler and Machinery Insurance:</strong> Coverage for
                  loss arising out of malfunction, the operation of pressure,
                  mechanical, and electrical equipment. Most property insurance
                  policies exclude these types of risks.
                </p>
                <p>
                  <strong>Bond:</strong> A three party contract that
                  guaranteeing that if the principal fails to perform as
                  obligated to, the obligee (person whom duty would be owed),
                  will be financially protected by the issuer of the bond. We
                  can assist you with your bond needs.
                </p>
                <p>
                  <strong>Broker:</strong> One who represents an insured and who
                  may render solicitation, negotiation, or services that deal
                  with a contract of insurance on behalf of the insured.
                </p>
                <p>
                  <strong>Broker of Record:</strong> Also known as Agent of
                  Record, this is a broker who is designated to handle certain
                  insurance contracts for the named insured.
                </p>
                <p>
                  <strong>Burglary and Theft Insurance:</strong> Coverage
                  against property losses due to burglary, robbery, or larceny.
                </p>
                <p>
                  <strong>Business Income Insurance:</strong> Provides
                  protection for a business owner against losses resulting from
                  a temporary shutdown of an insured peril. This provides
                  reimbursement for lost profits and necessary continuing
                  expenses. A common exposure that most people have added in
                  Florida, thanks to Hurricane Andrew.
                </p>
                <p>
                  <strong>Business Insurance:</strong> A commercial insurance
                  policy which provides coverage to a business. Many forms of
                  business insurance are available and depend on your specific
                  needs.
                </p>
                <p>
                  <strong>Builders Risk:</strong> A contract that will insure
                  building contractors for damage to property under
                  construction. Once erected, the Builders Risk policy may
                  usually be converted to a specific type of homeowners or
                  commercial property form.
                </p>
                <p>
                  <strong>Business Personal Property:</strong> Known as
                  “contents,” this refers to furniture, fixtures, equipment,
                  machinery, merchandise, and all other personal property owned
                  by the insured and used in the insured's business.
                </p>
                <p>
                  <strong>Buy Sell Agreement:</strong> An agreement made by the
                  part-owners of a business to purchase the interest of a
                  disabled or deceased owner. Values of the owner's share of the
                  business and the terms of the buying and selling are
                  established before death or at the beginning of a disability.
                </p>
